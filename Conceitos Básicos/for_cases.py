# Uso do FOR
# 4.1 Pizzas
pizzas = ['4 queijos', 'camarão', 'sensação']
for pizza in pizzas:
    print("I love " + pizza.title() + " pizza")
print("Eu amo todas as pizzas!")

# 4.2 Animais
animais=['cachorro', 'hammister', 'gato', 'elefante', 'zebra', 'tigre']
print("Lista original: " + str(animais))
animais.sort()
print("Lista Ordenada: " + str(animais))
print("Lista Original: " + str(animais))
animais.reverse()
print("Lista Invertida: " + str(animais))
print("Lista Original: " + str(animais))
